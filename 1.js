/*
Say Happy Birthday
This function will be called with a number representing a persons age and should return a string saying 'Happy Birthday, you are today!'

Examples:

exclaimWord(25);
// returns 'Happy Birthday, you are 25 today!'
exclaimWord(7);
// returns 'Happy Birthday, you are 7 today!'
*/

function sayHappyBirthday(number) {
    return "Happy Birthday, you are " + number + " today!"
}